			<h2>Recomendaciones para la adecuada participación de grupos con baja
				representación en congresos de matemáticas.</h2>
			<p>Es sabido que alrededor del mundo la participación de mujeres y
				otros grupos con baja representación en la matemática (y las
				ciencias exactas en general) es un problema importante a resolver.
				Como miembros de la comunidad matemática, la CEG tiene una
				responsabilidad ética de promover políticas de representación y
				participación equitativa para todos los grupos participantes en
				dicha profesión, promoviendo la inclusión de voces diversas y de
				diferentes puntos de vista, además de contribuir a disolver
				estereotipos históricamente anclados sobre el adecuado rendimiento
				de dichos grupos. La CEG considera también que estrategias como la
				selección de comités científicos y organizadores más equitativos,
				ponentes de conferencias magistrales, plenarias por invitación y por
				solicitud, deben incorporarse de manera incluyente, ya que ello
				contribuirá a la solución del problema de la participación de grupos
				subrepresentados en congresos. Creemos que promover la equidad en
				estos aspectos es importante, pues, como ya se ha estudiado, la
				presencia de mujeres en los comités científicos se correlaciona
				positivamente con el número de mujeres que son invitadas como
				conferencistas plenarias; además, se promueve la presencia de
				modelos a seguir, lo que también ayuda a que los investigadores
				pertenecientes a estos grupos, que ya son integrantes de la
				comunidad matemática, no vean sus carreras afectadas por su
				situación de invisibilidad.</p>
			<p>Con este fin, emitimos un documento que lleva el título de esta
				sección, el cual será distribuido a los organizadores de congresos
				nacionales, particularmente a aquellos que reciben apoyo económico
				de la SMM. El propósito del documento es crear conciencia entre la
				comunidad, así como proveer un marco para tomar acciones concretas
				en pos de la resolución de este problema. En resumen, lo que plantea
				el documento es que en los comités organizadores, científicos,
				paneles, conferencias por invitación y plenarias, se refleje (como
				mínimo) la distribución de porcentajes de acuerdo a la base de datos
