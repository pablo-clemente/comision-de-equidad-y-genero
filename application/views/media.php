            <h3>Faces of Women in Mathematics.</h3>
            <p>El video "Faces of Women in Mathematics" fue realizado por Eugenie Hunsicker, coordinadora del "London Mathematical Society’s Women in Mathematics Committee" y su hermana Irina Linke, cineasta, ellas produjeron un corto por el Día Internacional de la Mujer (8 de marzo) mostrando "rostros" de mujeres matemáticas a lo largo de todo el mundo. Cada mujer da su nombre y país y dice (en su propia lengua) que es matemática. Eugenie contactó al IMU Committee for Women in Mathematics (CWM), y este financió el proyecto. Se recibieron 149 cortos representando a 249 mujeres 37 países hablando 32 lenguas, todo esto se recopiló en menos de un mes. 
                
