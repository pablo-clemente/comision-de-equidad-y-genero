						A través de la Comisión de Equidad y Género de la Sociedad Matemática Mexicana, a cuatro mujeres que estén cursando o terminando la licenciatura en Matemáticas a concursar por una beca para concluir su tesis.
					</p>	
					<p>
						La asociación “Women in Combinatorics”, a través de la Comisión de Equidad y Género de la Sociedad Matemática Mexicana (CEG-SMM), tiene el gusto de comunicar que dispone de cuatro becas, con un monto de 500 USD cada una, para impulsar a estudiantes mexicanas a que terminen su tesis de licenciatura en Matemáticas y continúen con estudios de posgrado.
					</p>	
					<p>
						Esta convocatoria está abierta a mujeres (incluidas personas no binarias, trans y queer) que estén terminando el último año de estudios de una licenciatura en Matemáticas y trabajando en su tesis, o que se encuentren en proceso de obtener el título.
					</p>	
					<p>
						Esta Convocatoria da prioridad a mujeres identificadas como indígenas y pertenecientes a grupos con baja representación en las ciencias matemáticas, preferentemente de los estados de la República, fuera de la capital. Asimismo, se dará preferencia a las mujeres que estén desarrollando un proyecto en Combinatoria (no es requisito indispensable).
					</p>	
					<br>
					<h4> <strong>REQUISITOS:</strong></h4>
					<p>
						a.  Ser mujer (incluidas personas no binarias, trans y queer), identificarse como indígena o pertenecer a un grupo con baja representación en las ciencias matemáticas, especialmente de los estados de la República, fuera de la capital;
					</p>	
					<p>
						b. Estar realizando la tesis de licenciatura en Matemáticas o encontrarse en el proceso de titulación y estar inscrita en una institución nacional de educación superior;
					</p>	

					<p>
						c.  Contar con un promedio global de calificaciones no menor a 8.0 (ocho punto cero) o equivalente; y,</p>
					</p>
					<p>
						d. Manifestar la intención de realizar estudios de posgrado en Matemáticas.
					</p>	

					<h4><strong>Las solicitudes deberán incluir:</strong></h4>
					<p>
						1.       Un documento que acredite que la tesis de licenciatura se encuentra en desarrollo o que la estudiante ha iniciado el proceso de titulación y que está inscrita en una institución de educación superior;
					</p>
					<p>	
						2.       Copia (no necesariamente oficial) de su historial académico;
					</p>	
					<p>
						3.       Una breve descripción de su tema de tesis, los problemas matemáticos que se abordan y el nivel de creatividad de la misma (si se trata sólo de un resumen de un trabajo previamente desarrollado o si la estudiante realiza aportaciones matemáticas en un problema abierto);
					</p>	
					<p>
						4.       Una carta de recomendación, la cual deberá ser enviada directamente por quien la suscribe al correo electrónico de la Comisión de Equidad y Género (equidadygenero@smm.org.mx);
					</p>	
					<p>
						5.       Una breve exposición de motivos por los que se solicita el apoyo y la información de contacto de la solicitante. Esta exposición debe mencionar explícitamente en qué grupo de baja representación se debe considerar a la solicitante.
					</p>	
 					<p>
						Las solicitudes se recibirán vía correo electrónico, dirigido a la Comisión de Equidad y Género (<a href="mailto:equidadygenero@smm.org.mx" target="_blank">equidadygenero@smm.org.mx</a>), antes del viernes 30 de julio de 2021. La solicitud debe venir acompañada de un solo archivo en formato PDF, que incluya los documentos correspondientes. No se considerarán solicitudes incompletas.
					</p>	
					<p>
						La Comisión de Equidad y Género de la Sociedad Matemática Mexicana nombrará al Comité que evaluará las solicitudes. Los dictámenes de este comité son inapelables y se publicarán a más tardar el viernes 10 de septiembre de 2021. Cualquier punto no previsto en esta Convocatoria será resuelto por la propia Comisión.
					</p>
					<p>
						<strong>Nota importante:</strong> La beca se dividirá en tres pagos (un pago por mes) y al tipo de cambio en la fecha de la transferencia.
					</p>	
					
